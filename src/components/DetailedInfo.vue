<script setup>
import { ref, onMounted, watch } from 'vue';
import { useRoute } from 'vue-router';
import { queryVariables } from '../utils/searchUtils';

const route = useRoute();
const id = ref(route.params.id);

const title = ref("");
const year = ref("");
const demographic = ref("");
const min_age = ref(0);
const max_age = ref(100);
const description = ref("");
const variables = ref([]);

const parseDescription = (text) => {
  return text[0] === '"' ? text.slice(1, -1) : text;
}

const fetchData = async () => {
  console.log(`ID being queried: ${id._value}`);
  const result = await queryVariables(parseInt(id.value, 10));
  console.log(result)
  if (result !== undefined && result !== null && result.length > 0) {
    title.value = result[0][2];
    year.value = result[0][3];
    demographic.value = result[0][4];
    min_age.value = result[0][5];
    max_age.value = result[0][6];
    description.value = parseDescription(result[0][7]);
    variables.value = result.map((variable) => {
      return {name: variable[0], description: variable[1]}
    })
  }
}

onMounted(fetchData);

watch(() => route.params.id, (newId) => {
  id.value = newId;
  fetchData();
})

const viewData = () => {
  // maybe download as an excel or spreadsheet file
  console.log("data is viewed.")
}

const requestDataLink = 'https://docs.google.com/forms/d/e/1FAIpQLSc5SQeDOZv6hBp5gyCF5byiOri6_zCcU5gSk5Wj0PucAB52Dw/viewform'; // Replace with your actual link
</script>

<template>
  <div className="container">
    <div className="title-container">
      <h1>{{ title }}</h1>
      <a :href="requestDataLink" target="_blank" rel="noopener noreferrer">
        <Button label="Request Data" icon="pi pi-envelope" size="small" iconPos="right" rounded style="color: var(--text-color);"/>
      </a>
    </div>
    <div className="metadata-container">
      <h3><u>Year of Visit</u>: {{ year }}</h3>
      <h3><u>Demographic</u>: {{ demographic == "MOM" ? "Mothers" : "Children" }}</h3>
      <h3><u>Ages</u>: {{ min_age }} to {{ max_age }}</h3>
    </div>
    <div className="desc-container">
      <div className="inner-desc-container">
